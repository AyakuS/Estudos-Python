'''

Faça um Programa que peça o raio de um círculo, calcule e mostre sua área.

Calculo para área do circulo, é A = 3.14*(raio**2)

'''

print('=================== Carcúlo da Area do círculo! ===================')

raio = float(input('Digite o Raio do circulo: '))
a = 3.14*(raio**2)
print('A área do circulo é {}'.format(a))

